package com.pool.featurs8.FI.predicate;

public class Delivary {
	
	private Integer delivaryId;
	private String itemName;
	private Integer numberOfDaysFromDispatch;
	public Integer getDelivaryId() {
		return delivaryId;
	}
	public void setDelivaryId(Integer delivaryId) {
		this.delivaryId = delivaryId;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public Integer getNumberOfDaysFromDispatch() {
		return numberOfDaysFromDispatch;
	}
	public void setNumberOfDaysFromDispatch(Integer numberOfDaysFromDispatch) {
		this.numberOfDaysFromDispatch = numberOfDaysFromDispatch;
	}

}
